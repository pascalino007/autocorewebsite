// Utility: get unique values for filters
export function getUniqueColors(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.color))].sort();
}

export function getUniqueBrands(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.brand))].sort();
}

export function getUniqueBodyStyles(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.bodyStyle))].sort();
}

export function getUniqueTransmissions(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.transmission))].sort();
}

export function getUniqueFuelTypes(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.fuelType))].sort();
}

export function getUniqueConditions(vehicles: IUsedVehicle[]): string[] {
    return [...new Set(vehicles.map((v) => v.condition))].sort();
}
